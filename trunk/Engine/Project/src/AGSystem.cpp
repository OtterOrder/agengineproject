#include "AGSystem.h"
#include "AGGame.h"

extern	AGGame* gGame;

//------------------------------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------------------------------
void AGSystem::Init( void )
{
	gGame->Init();
}

//------------------------------------------------------------------------------------------------------------------------------
void AGSystem::Shutdown( void )
{
	gGame->Destroy();
	delete gGame;
}

//------------------------------------------------------------------------------------------------------------------------------
void AGSystem::MainLoop( void )
{
	while (true)
	{
		gGame->Update();
	}
}

//------------------------------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------------------------------
void main ()
{
	AGSystem::GetSingleton()->Init();
	AGSystem::GetSingleton()->MainLoop();
	AGSystem::GetSingleton()->Shutdown();
}
