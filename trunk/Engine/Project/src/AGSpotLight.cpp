#include "AGSpotLight.h"

//------------------------------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------------------------------
AGSpotLight::AGSpotLight()
: AGLight (Spot)
{
}

//------------------------------------------------------------------------------------------------------------------------------
AGSpotLight::~AGSpotLight()
{
}

//------------------------------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------------------------------
void AGSpotLight::Update()
{
	AGVector3fNormalize(mDirection);
	mInHalfAngle  = AGClamp(0.f, AGPi, mInHalfAngle);
	mOutHalfAngle = AGClamp(0.f, AGPi, mOutHalfAngle);
}