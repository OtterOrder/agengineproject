#include "AGTexture.h"

#include "AGWDX9.h"

//------------------------------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------------------------------
AGTexture::AGTexture()
{
	_mPTexture = NULL;
}

//------------------------------------------------------------------------------------------------------------------------------
AGTexture::~AGTexture()
{
}

//------------------------------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------------------------------
AGResource::AGResourceResult AGTexture::Load (cStr _FileName, void* _Parameters)
{
	Release();

	AGLoadTexture(_FileName, _mPTexture, _mTextureInfo);

	return RES_SUCCEED;
}

//------------------------------------------------------------------------------------------------------------------------------
void AGTexture::Release ()
{
	SAFE_RELEASE(_mPTexture);
}