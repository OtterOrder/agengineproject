#include "AG3DEntity.h"

//------------------------------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------------------------------
AG3DEntity::AG3DEntity()
{
	_mpParent = NULL;

	mPosition		= AGVector3f(0.f, 0.f, 0.f);
}

//------------------------------------------------------------------------------------------------------------------------------
AG3DEntity::~AG3DEntity()
{
}

//------------------------------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------------------------------
void AG3DEntity::Update ()
{
	AGMatrixTranslation(&_mWorld, mPosition.x, mPosition.y, mPosition.z);
}